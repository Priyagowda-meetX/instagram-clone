import SwiftUI
import RswiftResources

/// Namespace for Default App Font Weights
public enum AppFontWeight {
	/// FontWeight value of `w900`
	public static let black: Font.Weight = .black
		
	/// FontWeight value of `w800`
	public static let extraBold: Font.Weight = .heavy
		
	/// FontWeight value of `w700`
	public static let bold: Font.Weight = .bold
		
	/// FontWeight value of `w600`
	public static let semiBold: Font.Weight = .semibold
		
	/// FontWeight value of `w500`
	public static let medium: Font.Weight = .medium
		
	/// FontWeight value of `w400`
	public static let regular: Font.Weight = .regular
		
	/// FontWeight value of `w300`
	public static let light: Font.Weight = .light
		
	/// FontWeight value of `w200`
	public static let extraLight: Font.Weight = .ultraLight
		
	/// FontWeight value of `w100`
	public static let thin: Font.Weight = .thin
}
